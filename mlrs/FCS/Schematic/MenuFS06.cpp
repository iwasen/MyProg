// CMenuFS06 メッセージ ハンドラ

//*****************************************************************************************************
//  1. 関数名
//		CMenuFS06::OnInitDialog
//----------------------------------------------------------------------------------------------------
//  2. 機能
//		ダイアログ初期化処理
//----------------------------------------------------------------------------------------------------
//  3. パラメータ
//		無し
//----------------------------------------------------------------------------------------------------
//  4. 戻り値
//		BOOL	TRUE
//*****************************************************************************************************
BOOL CMenuFS06::OnInitDialog()
{
	CMenuCommon::OnInitDialog();

	// 動作ステータス設定
	SetStatus(STATUS_PANEL_OFF, STATUS_POWER_OFF);

	return FALSE;
}

//*****************************************************************************************************
//  1. 関数名
//		CMenuFS06::OnBnClickedButtonAction1
//----------------------------------------------------------------------------------------------------
//  2. 機能
//		「ＯＮ」ボタンクリック処理
//----------------------------------------------------------------------------------------------------
//  3. パラメータ
//		無し
//----------------------------------------------------------------------------------------------------
//  4. 戻り値
//		無し
//*****************************************************************************************************
void CMenuFS06::OnBnClickedButtonAction1()
{
	// 動作ステータス設定
	SetStatus(STATUS_PANEL_ON, m_ePowerStatus);

	// Flashへ操作指示送信
	SendAction(1);
}

//*****************************************************************************************************
//  1. 関数名
//		CMenuFS06::OnBnClickedButtonAction2
//----------------------------------------------------------------------------------------------------
//  2. 機能
//		「入」ボタンクリック処理
//----------------------------------------------------------------------------------------------------
//  3. パラメータ
//		無し
//----------------------------------------------------------------------------------------------------
//  4. 戻り値
//		無し
//*****************************************************************************************************
void CMenuFS06::OnBnClickedButtonAction2()
{
	// 動作ステータス設定
	SetStatus(m_ePanelStatus, STATUS_POWER_ON);
}

//*****************************************************************************************************
//  1. 関数名
//		CMenuFS06::OnBnClickedButtonAction3
//----------------------------------------------------------------------------------------------------
//  2. 機能
//		「リセット」ボタンクリック処理
//----------------------------------------------------------------------------------------------------
//  3. パラメータ
//		無し
//----------------------------------------------------------------------------------------------------
//  4. 戻り値
//		無し
//*****************************************************************************************************
void CMenuFS06::OnBnClickedButtonAction3()
{
	// 動作ステータス設定
	SetStatus(STATUS_PANEL_OFF, STATUS_POWER_OFF);

	// Flashへ操作指示送信
	SendAction(3);
}

//*****************************************************************************************************
//  1. 関数名
//		CMenuFS06::SetStatus
//----------------------------------------------------------------------------------------------------
//  2. 機能
//		動作ステータス設定処理
//----------------------------------------------------------------------------------------------------
//  3. パラメータ
//		EPanelStatus	ePanelStatus		[I] 射撃統制パネル電源動作ステータス
//		EPowerStatus	ePowerStatus		[I] 電源併入動作ステータス
//----------------------------------------------------------------------------------------------------
//  4. 戻り値
//		無し
//*****************************************************************************************************
void CMenuFS06::SetStatus(EPanelStatus ePanelStatus, EPowerStatus ePowerStatus)
{
	// 動作ステータス設定
	m_ePanelStatus = ePanelStatus;
	m_ePowerStatus = ePowerStatus;

	// メニューボタン設定
	SetMenuButton();
}

//*****************************************************************************************************
//  1. 関数名
//		CMenuFS06::SetMenuButton
//----------------------------------------------------------------------------------------------------
//  2. 機能
//		メニューボタン設定処理
//----------------------------------------------------------------------------------------------------
//  3. パラメータ
//		無し
//----------------------------------------------------------------------------------------------------
//  4. 戻り値
//		無し
//*****************************************************************************************************
void CMenuFS06::SetMenuButton()
{
	switch (m_ePowerStatus) {
	case STATUS_POWER_OFF:
		m_cButtonAction1.SetEnableAndCheck(FALSE, FALSE);
		m_cButtonAction2.SetEnableAndCheck(TRUE, FALSE);
		break;
	case STATUS_POWER_ON:
		m_cButtonAction1.SetEnableAndCheck(m_ePanelStatus == STATUS_PANEL_OFF, m_ePanelStatus == STATUS_PANEL_ON);
		m_cButtonAction2.SetEnableAndCheck(FALSE, TRUE);
		break;
	}
}
