BOOL CMenuFS13::OnInitDialog()
{
	CMenuCommon::OnInitDialog();

	// 動作ステータス設定
	SetStatus(STATUS_INITIAL);

	return FALSE;
}

//*****************************************************************************************************
//  1. 関数名
//		CMenuFS13::OnBnClickedButtonAction1
//----------------------------------------------------------------------------------------------------
//  2. 機能
