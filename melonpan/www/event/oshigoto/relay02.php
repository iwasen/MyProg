      <p class="text12b">■カウンセラー不要の環境づくり？！〜カウンセラーの仕事〜</p>
      <p class="text12">カウンセラーをしていると、クライエント（来談者）から、「毎日他人の悩みを聞いていて、疲れませんか？」と問われることがあります。わたしは、たいてい、「そんなに疲れませんよ」と答えます。本当に疲れないのです。</p>
      <p class="text12">実は、カウンセリングが終わると、かえって元気が出ることさえあります。どうしてなのかと、ふと考えてみるのですが、それは、悩みを抱えながらも一所懸命生きているクライエントさんの姿を見、話を聞かせていただくうちに、逆にわたし自身が励まされるからなのです。ありがたいことです。</p>
      <p class="text12">一方、がっくり疲れるのは、ここまで深刻になる前にもう少し早めに周りのだれかが対応してくれていれば……という思いに駆られるときです。それも、ちょっとした誤解が原因で、もはやあと戻りできない状態になってしまったという話を聞くと、カウンセリングというものの限界を感じざるをえません。もう一つ残念に思うのは、カウンセリングによってせっかく気持ちが上向きになっても、またいつもの環境（人間関係）に帰ると、すべてが元に戻ってしまうというケースです。</p>
      <p class="text12">これらを改善するには、日常の人間関係、というか、その人を取り巻く環境（家庭、職場、学校など）が改善される必要があります。言い換えれば、悩んでいる本人だけでなく周りの人も変わる必要があるということです。悩みの多くは、一方的な人間関係から生まれます。しかし、カウンセリングによって、少なからず悩みが和らぐのですから、日常の中のほんの一部でもいいから、カウンセリングのような環境があれば、悩みは減少します。</p>
      <p class="text12">これは、従来から、カウンセリング・マインドや傾聴などと称され、本もたくさん出ています。しかし、いま一つ広がりません。その一番の理由は、たとえ頭でわかっても、練習をして身に付けないと使いものにならないからです。そこで、わたしは、そんな状況にささやかな一石を投じようと、カウンセリングと並行して、〈対話法〉という技法の普及活動を行っています。</p>
