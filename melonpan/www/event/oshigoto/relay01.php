      <p class="text12">みなさんこんにちは。地方の某放送局でアナウンサーをしているまると言います。どうぞよろしくお願いします。　m(_ 
        _)mペコ　<br>
        今回は生放送の番組の中で、アナウンサーは映っていない時間に一体何をしているものなのか、ちょっぴりご紹介したいと思います。</p>
      <p class="text12">生放送はやり直しがききません。まるもテレビの生番組をやってますが、オープニングの直前まで打ち合わせしてたり、順番の確認をしてたり、原稿を読む練習をしてたり・・。それこそ本番の３０秒前になっても席に座ってない、なんてこともザラなんです。(笑）</p>
      <p class="text12">さてさて、そんなふうに十分準備をした上で、いよいよ番組スタート！</p>
      <p class="text12">ところが・・・。準備バッチリがゆえに、始まっちゃうとほとんどすることがないんです。必要な資料も手元に揃ってるし、原稿の順番も、読む練習もＯＫ！つまり、本番中も出番以外はヒマ！！！(笑）</p>
      <p class="text12">スタッフのみんなが走り回って頑張ってる中、まるだけヒマそうにしてるのはなんだか申し訳ないような気がします。でも衣装を汚しちゃいけないのでＡＤスタッフを手伝うことはできません。そして専門の知識もないから、技術スタッフを手伝うこともできません。</p>
      <p class="text12">結局、おとなしく座って出番を待つしかないんです・・。(笑）</p>
      <p class="text12">生放送のテレビ番組は、事前収録のものよりかなり緊張します。放送された部分以外のところに苦労があったり笑いがあったり、ときにはごまかしがあったりしますが（←悪い意味じゃなくてネ＾＾；）視聴者のみなさんにとっては、放送されたものが全てですからネ。</p>
      <p class="text12">まるのメルマガは主にこういった舞台裏を書いてます。イロイロと暴露（？）してますのでゼヒ読んでみてくださいね！</p>
