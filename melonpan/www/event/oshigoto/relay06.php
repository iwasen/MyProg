      <p class="text12b" align="left">■アメリカ人の商品購入時の意識調査で驚いた事<br>
〜海外駐在の商品企画マンのお仕事〜</p>
      <p class="text12">社命で米国に8年間滞在中アメリカ人の購買意識調査をした時のことだ。<br>
調査会社の人間が司会となり、10人ほど無作為に選ばれた中流の所得層の人に座談会形式で我々の知りたい項目を元にフリーディスカッションしてもらった。</p>
      <p class="text12">司会者の質問のひとつに、「耐久消費財を購入する際実際に購入するまでにどの位の時間を費やしますか？」というのがあった。<br> 出席者のうち4人が「6ヶ月位掛ける。」と答えそのうちの一人が、6ヶ月間をどう使うかをとうとうと述べた。　それを要約すると、</p>
      <p class="text12">１．夫婦で幾つかの小売店に行き展示している商品を一通り見て周り候補になりそうな商品を抽出する。　店員の話は信頼できないから参考にしない。</p>
      <p class="text12">２．選んだ商品のメーカーにカタログを請求し性能・細部の機能などの検討・
　　比較をする。　又コンシューマーレポート等信頼できる機関のテストレポ
　　ートを参考にする。　商品について不明な点はメーカーに問い合わせる。</p>
      <p class="text12">３．以上で購入する商品を決めるが、小売店数店に行き販売価格を調べると同
　　時にいつスペシャルセールがあるのかを調べる。(ここまでに3ヶ月。）</p>
      <p class="text12">４．最も安く買えるまで待つ。平均して3ヶ月くらいは待つ必要がある。</p>
      <p class="text12">というのである。　他の3人はそれに肯いていたので概ね同意していたと思う。</p>
      <p class="text12">我々日本人と比較すると悠長と言うか、或いは余りにも賢い消費者というか、
衝動買いを期待できない国民性なのだとつくづく感じ入った。<br>
米国は価格破壊が真っ先に発生した国であり、米国で販売が継続できれば大成
功と言われた事があるが、ここまで慎重に購入決定するとなると充分肯ける。<br>
因みに英語で衝動買いを、Impulse-Buyと言うが、後日あるアメリカ人の友人
に衝動買いのことを尋ねたら、「大金持ち」と「頭の弱いやつ」がする買い方
さ！　とこともなげに言われ、大変大きなカルチャーショックを感じた。</p>
