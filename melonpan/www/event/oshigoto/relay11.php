      <p class="text12b" align="left">■重役は孤独な王様</p>
      <p class="text12">私は名前を聞けば誰でも知っている大企業の取締役をしていました。皆さんは重役と言うとどのようなイメージをお持ちでしょうか？大きな家に住み、お車のお出迎え、雲の上の人ですか？</p>
      <p class="text12">初めて「お車」が迎えに来た日にはとても嬉しく「遂に雲の上に到達したか」と感無量でした。そして一流の人に会え、一流のモノに触れることが出来たの今でも貴重な財産だと思っています。しかし初めて「お車」が来た日から「私は私であって私で無い」ことになりました。地方の小さな都市の名士となり、眠っている時以外の全ての時間から「私的な部分」を奪われました。極めて大げさに言えば「皇族のご苦労が良くわかります」</p>
      <p class="text12">そして華やかな外見とは裏腹に、本当はとても孤独なのです。<br>
私は四千人の組織のトップとして経営に当たりました。業績が悪い場合にはとても苦しい決断をしなければなりません。苦渋の決断をする時、従業員と家族そして大切なお客様等々、一万人以上の人々の顔が脳裏を駆け巡ります。<br>
私を支える経営幹部が固唾を飲んで、私の決断を待つ瞬間の孤独さは言葉では言い表せません。</p>
      <p class="text12">私は気さくさと陽気なことで有名でしたが、ある日、「藤原さん、貴方は相当訓練して明るさを保とうとしていますね」と言われました。「どうして」と聞くと「顔は笑っていますが目が笑っていません」との答え。本当にハッとしました。「自分の身体であって自分の身体でない」言いようのない孤独それが重役です。</p>
      <p class="text12">このような孤独から開放されて一年半、自由を謳歌しているうちに、私の現役時代と同じ苦しみが新聞紙上に見られることが多くなりました。私の経験が少しでも世の中のお役に立てばとメルマガを四つも立ち上げました。とても好評です。「ビジネスマン必見」などのお便りを頂くと、大変に嬉しく、今は全く孤独ではありません。</p>
