      <p class="text12b">■「やっていてよかった！」〜コピーライターの醍醐味とは？</p>
      <p class="text12">私は現在、四国の某都市で新聞のレギュラー記事などに携わる現役のライターです。今はフリーであれこれ仕事をしていますが、３年前会社員だったころ、私が所属していた広告制作プロダクションの社長についてお話しましょう。</p>
      <p class="text12">某ゼネコンをメインにクライアントを持つ彼は、もともとコピーライター。東京芸大卒という輝かしい学歴を持つ彼ですが、外見からはクリエーターを思わせないほど普通の人。営業が苦手だけど、類をまれに見ない才能の持ち主である彼は、創るものにいつも喝采を浴びるほど、イイモノを仕上げていました。しかし、ここ数年の不況が続き、そのゼネコンからの発注も少なくなり、会社自体の運営もかなりマズイ状況。もうこれまでかと言われていたある日。そのゼネコンの担当者から電話がかかってきました。</p>
      <p class="text12">仕事の依頼は、業界新聞に掲載する新聞広告の制作。いつも通り、快くひきうけた内容は、&quot;空港受注&quot;をかけた新聞広告でした。大きさはホントウに小さなモノでして、まさにコピー勝負のもの。10年以上という長年の付き合いなので、たったの15,000円で版下入稿まで引き受け、業界新聞社に入稿し、私は制作管理を担当させていただきました。</p>
