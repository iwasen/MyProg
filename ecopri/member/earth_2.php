科学者によると、温室効果ガスの中でも、もっとも気温の上昇に影響を与えているのは二酸化炭素だそうです。1998年の空気中の二酸化炭素濃度を1750年と比較すると30%以上増加しているそうです。この1750年という年は、よく地球温暖化の議論の中で登場してくる年ですが、その理由はこれが産業革命が始まるときだからです。私たち人間は産業革命以降、大量の二酸化炭素を排出するようになり、現在の地球温暖化をまねいたのです。そこで
1750年は産業革命の前の年として比較対象にされることが多いのです。 </td>
