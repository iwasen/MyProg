<td align="center"> <font color="#0078FF">〜なぜ地球は暖かくなるの？〜</font></td>
</tr>
<tr>
<td>地球は空気で覆われています。この空気の中には窒素（約78%）や酸素（約21%）が含まれていますが、わずかに二酸化炭素、メタン、一酸化二窒素などの温室効果ガスと呼ばれるものも含まれています。<br>
<br>
太陽から地球に届く光は、空気を通過して地面を暖めます。そして暖められた地面は宇宙に向かって熱を放出します。このとき、空気の中にある温室効果ガスは地面からの熱を吸収して暖まり、熱の一部を再び地面に向かって放射します。そのため、地球はすべての熱を宇宙に出してしまうことなく、生物が生きるのに適した平均気温約15℃を保つことができるのです。ちょうど温室で野菜を栽培するときのように、温室効果ガスが地球を温室のように暖めてくれています。<br>
<br>
このように、空気の中に含まれている温室効果ガスは重要な役割を果たしています。もしも、この温室効果がなかったら、地球の表面の平均気温は零下18℃になると言われていますから、地球はとても寒い星となり、ほとんどの生き物が生きられない世界になってしまいます。<br>
<br>
温室効果は地球を暖かく住みやすい環境にするために大切な役割を果たしています。長い間、温室効果ガスは地上から宇宙に向かって放出される熱を適度に地球の表面に向かってはね返してくれていたので、私たちは生活するのに適した気候の地球に住むことができました。ところが、近年、空気中の二酸化炭素が増えてきたため、地球にはね返される熱の量が増えてきて、結果として地球の平均気温が急速に上昇しています。<br>
<br> </td>
