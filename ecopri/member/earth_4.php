<tr>
<td>地球温暖化の問題に取り組もうとする動きは1980年代後半から始まり、世界の科学者と国連が中心となって議論が始まりましたが、1990年代になって国際条約という形で各国代表が政治的に話し合うという形に進んできました。しかし、政治的な議論となると、世界の国々はそれぞれの立場があって、なかなか意見をまとめることはできませんでした。1997年12月に京都で開かれた『地球温暖化防止京都会議』では日本が議長国となり、各国の利害を越えて『京都議定書』が採択されました。『京都議定書』では、先進国における温室効果ガス（二酸化炭素など6種）の排出量の具体的な削減目標が決められており、日本も2008〜2012年の間に1990年と比較して6%の削減を義務づけられました。<br>
<br>
このため日本でも、国のレベルでは具体的な数値目標を達成するために、さまざまな政策が行われているところです。国も、地方自治体も、企業も、それぞれが温室効果ガスの排出を減らす努力を始めようとしています。しかし、それだけで安心してはいられません。というのも、二酸化炭素をはじめとする温室効果ガスの排出は、すぐには止められないのです。温室効果ガスを出している工場をすべて閉鎖してしまうわけにはいかないし、それらの工場設備を環境にやさしい設備と交換するには莫大なお金がかかり、企業はすぐに対応できません。また、温室効果ガスの排出を削減できるからと言って公共交通機関を止めてしまうわけにもいかないし、今、走っているガソリン自動車を電気や太陽電池で動く自動車に買い換えてもらうには、まだまだ時間がかかるのです。</td>
