<td valign="top">・気候の変動<br>
ツバルの例のように、海面上昇は、島国諸国や低地を持つ国々にとって、とても深刻な問題になっていますし、日本にも大きな影響があることが予測されています。また、エルニーニョ現象、干ばつ、洪水、異常高温などの異常気象が、すでに世界各地で起きています。このような異常気象の発生も含めて、気候・気象に変化が起きることが予測されています。また、海氷が溶けることで、海水の塩分濃度が下がり、そのことで海流などへの悪影響も心配されています。<br>
</td>
