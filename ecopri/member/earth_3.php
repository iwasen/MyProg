<tr>
<td>地球温暖化が進むと私たちの環境がどう変わるのでしょうか。現在、過去1000年でもっとも高い平均気温になってしまい、さらに気温が上昇している状況では、私たちの環境が今後どのように変化するかを確実に予測することはできません。しかし、科学者たちは予測できる地球温暖化による影響を挙げて警告しています。</td>
