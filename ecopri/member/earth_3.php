<td>・人間と社会環境への影響<br>
気候の変動は人間の健康に大きな影響を与えます。日本でも夏に熱中症、脱水症で倒れた人々についての報道が増えてきましたが、そのような直接的な影響の他にも、気温や降水量が変化することで、病気を媒介する動物が増加したり、病原菌が増加するなど、感染症が広がる恐れもあります。蚊が増えれば、マラリア、デング熱、黄熱病、脳炎などの病気が広がることも考えられますし、気温が上がればコレラやサルモネラ感染症の患者も増えると推測されています。また、気候変化は食料生産にも深刻な影響を与える可能性があり、飢餓、飢饉の危険性が増大するとも言われています。経済的にも、温暖化被害への対策などに費用がかかることが予測されています。</td>
