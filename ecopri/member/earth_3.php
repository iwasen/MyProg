・自然環境への影響<br>
動物も植物も、それぞれ生きるために適した環境というものがあり、その環境に適応して生活しています。しかし、急激に気候変化が起こると、植物も動物も適応できずに絶滅に瀕することもあり、また、逆に一部の種が急激に増加することで自然界のバランスを崩し、他の種を減少させることも考えられます。<br>
</td>
