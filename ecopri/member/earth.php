<td align="center"> <font color="#0078FF">〜地球温暖化って・・・なに？〜</font></td>
</tr>
<tr>
<td>私たちが住んでいる星、地球は暖かくなってきています。日本でも、夏には熱帯夜で眠れない夜が増えてきたり、熱中症で倒れる人が報道されたりしています。冬にあまり雪が降らなくなってきた地方もあります。よく考えてみると、私たちは日常生活の中で、気候がだんだん暖かくなってきていることを体験しています。これらも地球温暖化の影響の一つと言えるのですが、夕べの熱帯夜と地球温暖化を結びつけて考えることは少ないと思います。地球温暖化の影響が私たちの周りで起きていても、私たちはつい見過ごしてしまいそうになります。そこで、このページでは地球温暖化とはどんなことなのかを考えてみたいと思います。<br>
<br>
地球の長い歴史の中でも、今、世界の平均気温は急速に上昇しています。科学者たちによると20世紀の100年間に地球の平均気温は0.6℃上昇しました。そして21世紀の100年間では1.4〜5.8℃上昇することが予測されています。「0.6℃？　たいしたことないじゃない。」と思うかも知れませんが、平均気温が0.6℃上昇するということは地球にとっては深刻な変化です。世界の各地では、すでに重大な影響が出始めています。<br>
<br>
南太平洋の真ん中、ちょうどハワイとオーストラリアの間にツバルという小さな島国があります。ツバルは珊瑚礁の島9つでできている国です。すべての島の面積を合わせても伊豆大島の4分の1ほどしかなく、人口は11,000人の小さな国です。そして地面の平均標高は1.5m。太平洋にぽっかりと浮かぶ平和な楽園のイメージそのままです。<br>
<br>
しかし、いま、ツバルは『世界で最初に沈む国』と呼ばれ、国民は『環境難民』と呼ばれています。その理由は、このまま地球温暖化が進むと、ツバルは海に沈んでしまうからです。ツバルの人々は、住み慣れた土地を離れ、どこか他の国に移住する移住することを迫られています。</td>
