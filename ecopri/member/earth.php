<td>地球が暖かくなってきたために海に浮かぶ氷が溶け始めています。20世紀に0.6℃上昇したと言いましたが、すでにその分の氷が溶けて海水が増えたり膨張したりして、海面が10〜20cm上昇したと言われています。そして21世紀にはさらに氷は溶けだして、海面は最大で88cm上昇すると予測されています。<br>
<br>
ツバルだけでなく、モルジブ、マーシャル諸島、キリバス、トンガなど標高が低い島国諸国（地球温暖化により影響を受ける小さな島国は、小島嶼国連合AOSISを結成して、地球温暖化防止の陳情活動などを行っている。)は、海面が上昇すると国が海に沈むんでしまうという危機に直面しています。<br>
<br>
海面の上昇の影響を受けるのは島国諸国だけではありません。標高が低い国土を持った他の国々でも、国民が住むところを失ったり、農地が水没して経済的な被害、食料不足などが予測されたりしています。海面が1m上昇すると、日本でも満潮のときに海面の下になってしまう土地が2,339平方キロメートルになり、410万人が被害を受けると言われています。<br>
<br>
これは科学者たちが予測している地球温暖化の影響の一部ですが、他にも様々な影響が予測されていますし、予測できない影響もあると考えられています。そして問題なのは、こうしている今も地球全体の温度は上昇していて、将来、私たちが受ける被害も大きくなっていくということです。
