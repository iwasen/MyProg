class ModifyStoriesInputView extends SmartyView
{
    public function execute ()
    {
        $controller = $this->getContext()->getController();
        $request = $this->getContext()->getRequest();
        $user = $this->getContext()->getUser();

        $this->setTemplate('modifyStories.html');

        // assign
        $attributeNames = $request->getAttributeNames();
        foreach($attributeNames as $key){
            $var = $request->getAttribute($key);
            $this->setAttribute($key, $var);
        }
        
        $this->useMainTemplate();
    }
}

class ModifyStoriesSuccessView extends SmartyView
{
    public function execute ()
    {
        $controller = $this->getContext()->getController();
        $request = $this->getContext()->getRequest();
        $user = $this->getContext()->getUser();

        $this->setTemplate('result.html');

        // assign
        $attributeNames = $request->getAttributeNames();
        foreach($attributeNames as $key){
            $var = $request->getAttribute($key);
            $this->setAttribute($key, $var);
        }
        
        $blog_category_id = intval($request->getParameter('blog_category_id'));
        $url = 'index.php?module=Blog&amp;action=adminShowStories&amp;navipage_id='.hitobito::getNaviPageInfo()->getId();
        $url .= ($blog_category_id > 0) ? '&amp;blog_category_id='.$blog_category_id : '';
        
        $this->setAttribute('return_url', $url);
        
        $this->useMainTemplate();
    }
}

